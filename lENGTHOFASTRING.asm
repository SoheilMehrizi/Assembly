;; CALCULATING THE LENGTH OF A STRING


ORG 100H

.DATA

STR1 DB 10,13,"I HAVE 30$"
LEN DB 00H


.CODE
MOV AX, @DATA

MOV DS,AX

MOV SI,0102H ; OFFSET OF STR1

MOV BL,0

COUNT_CHARACTER:
LODSB ;LOD NEXT CHARACTER

OR AL,AL ;NULL CHARACTER TEST
JZ END_OF_STRING

JB COUNT_CHARACTER ; CONTROLL CH < 32H

INC BL

JMP COUNT_CHARACTER

END_OF_STRING:
MOV LEN, BL

RET

