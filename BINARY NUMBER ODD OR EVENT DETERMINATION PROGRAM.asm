ORG 100H
.DATA 
MYODD DB "0DD$"
MYEVEN DB "EVEN$"

BINARY DB 59H ; EX_NUMBER


.CODE

START:

MOV AL,BINARY
SHR AL,1
JC IS_ODD ; CALLING THE FUNCTION

LEA DX,MYEVEN
JMP PRINT ; CALLING PRINT FUNCTION

IS_ODD: ; CHECKS IF THE NUMBER IS ODD OR NOT

LEA DX,MYODD

PRINT:

MOV AH, 9H
INT 21H ; GETS A NUMBER FROM COMMAND

MOV AH,4CH ;EXIT DOS

INT 21H

RET
