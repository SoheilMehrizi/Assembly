ORG 100H
.DATA
ODD_MESSAGE DB "ITS ODD$"
EVEN_MESSAGE DB "ITS EVEN$"

BINARY 63H ;INSTANCE NUMBER

.CODE

START:

MOV AL, BINARY

SHR AL,1

JC ITS_ODD

LEA DX, EVEN_MESSAGE
JMP PRINT_MESSAGE

ITS_ODD:
    LEA DX, ODD_MESSAGE
    JMP PRINT ; JMPING TO LABLE : PRINT
    
PRINT:
MOV AH ,09H
INT 21H ; PRINTING THE MESSAGE

MOV AH,4CH ;EXIT TO DOS

RET