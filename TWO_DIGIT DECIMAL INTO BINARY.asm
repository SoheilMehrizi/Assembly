org 100h

.data

MSG1 DB "ENTER NO:$"

DIGIT1 DB ?
DIGIT2 DB ?
BINARY DB ?

.code

START:

LEA DX,MSG1
MOV AH,9
INT 21H
; GET DIGIT 1

MOV AH,1
INT 21H 

; CONVERT ASCII TO BCD

SUB AL,30H
MOV DIGIT1,AL
;GETTING DIGIT2
MOV AH,1
INT 21H

;CONVERTING 2ND DIGIT FROM ASCII INTO BCD

SUB AL,30H

MOV DIGIT2,AL

;RETUTRNING BOTH DIGITS INTO ACCUMULATOR FOR CONVERTING ASPECT

MOV AL,DIGIT1
MOV AH,DIGIT2

MOV BL,AH
MOV CL,10
MUL CL

ADD AL,BL
;;;;;;;;;;;;;;;;;;;;;;
MOV BINARY,AL

MOV AH,4CH ; EXIT INTO DOS
INT 21H
RET