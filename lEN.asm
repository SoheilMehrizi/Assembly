; LENGTH OF A STRING


ORG 100H
.DATA

STR1 DB 10,13, "I HAVE 30$"

LEN DB 00H

.CODE
MOV AX, @DATA

MOV DS, AX
MOV SI,0102H

MOV BL,0

COUNT_CHARACTER: ; DEFINING THE PROCEDURE

LODSB ;LOAD NEXT CHAR

OR AL,AL ; NULL CHAR TEST , 0 OR 0 : 0

JZ END_OF_STRING ; JUMP IF ZERO INTO END_OF_STRING PROCEDURE

CMP AL,20H ; CONTROL CH<32H

JB COUNT_CHARACTER

INC BL

JMP COUNT_CHARACTER

END_OF_STRING:
    MOV LEN,BL
    
RET